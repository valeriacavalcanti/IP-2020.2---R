'''
    Objetivo: Ler dois número inteiros e informar se o primeiro número lido é maior ou não.
'''

n1 = int(input())
n2 = int(input())

if (n1 > n2):
    print(n1, 'é o maior')
    print('verdadeiro')
    print('eita')
    print('que bom')
else:
    print(n1, 'não é o maior')
    print('falso')
    print('humm')
    print('que chato')
